DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC

MOV AH,1
INT 21H           ;INPUT 1ST NUMBER
MOV BL,AL

MOV AH,1
INT 21H           ;INPUT 2ND NUMBER
MOV CL,AL

ADD BL,CL         ;ADD TWO NUMBERS
SUB BL,48         ;BALANCING ASCII CODE

MOV DL,BL
MOV AH,2          ;PRINTS ADDITION OF TWO NUMBERS
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
