DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
v1 DB 'ENTER A NUMBER$'
v2 DB 'THE NUMBER YOU ENTERED IS EVEN$'
v3 DB 'THE NUMBER YOU ENTERED IS ODD$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV DX,OFFSET v1 ;PRINT
MOV AH,9
INT 21H


MOV AH,1 ;INPUT DIVIDENT FROM USER
INT 21H


MOV BL,2  ;DIVISOR
DIV BL
CMP AH,0 
JE EVENN



MOV DX,OFFSET v3  ;PRINT 
MOV AH,9
INT 21H

MOV AH,4CH
INT 21H
EVENN:
MOV DX,OFFSET v2
MOV AH,9
INT 21H
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
