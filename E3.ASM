;PROGRAM TO DRAW A SHAPE WITHIN A SHAPE
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'SQUARE$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV AH,6         
MOV AL,10        
MOV BH,00110000B
MOV CH,0         
MOV CL,0
MOV DH,35        
MOV DL,35
INT 10H

;FOR INNER SHAPE

MOV AH,6        ;SET FUNCTION
MOV AL,8        ;SET NO OF LINES
MOV BH,00100000B ;COLOR
MOV CH,0         ;STARTING POINT
MOV CL,0
MOV DH,25        ;SET BOTTOM AND LEFT
MOV DL,25
INT 10H


LEA DX,MSG       ;PRINT PROMPT
MOV AH,9
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN