DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC

MOV DL,'L'
MOV AH,2            ;PRINT 1ST SINGLE CHARACTER
INT 21H

MOV DL,'A'
MOV AH,2            ;PRINT 2ND SINGLE CHARACTER
INT 21H

MOV DL,'H'
MOV AH,2             ;PRINT 3RD SINGLE CHARACTER
INT 21H

MOV DL,'A'
MOV AH,2             ;PRINT 4TH SINGLE CHARACTER
INT 21H

MOV DL,'N'
MOV AH,2             ;PRINT 5TH SINGLE CHARACTER
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
