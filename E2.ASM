;program to draw a rectangle with a name written inside
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
MSG DB "RECTANGLE$"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV AH,6        ;SET FUNCTION

MOV AL,10       ;SET NO OF LINES
MOV BH,01000000B ;COLOR
MOV CH,0        ;STARTING POSITION
MOV CL,0

MOV DH,55       ;SET BOTTOM AND LEFT
MOV DL,55
INT 10H

LEA DX,MSG      ;PRINT PROMPT
MOV AH,9
INT 21H



MOV AH,4CH
INT 21H



MAIN ENDP
END MAIN